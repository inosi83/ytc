package org.comstudy21.ex01;
/**
 * 문서화 시킬수 잇는 JAVA 전용주석
 * 이것은 여러줄 주석 가능
 * @작성자 : 김정환
 */

public class Hello01 {

	public static void main(String[] args) {
		
			//이것은 한줄 주석
			//줄을 바구면 자동으로 주석 해제
		System.out.println("hello world!");
		System.out.print("성명:");
		System.out.println("홍길동");

	}

}
/*
이것은 여러줄 주석
줄이 바뀌어도 주석이 유지된다.
*/